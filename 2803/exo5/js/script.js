// creer une variable de type number avec la valeur 10 
/*opérateur d'affectation
*/
var myNumber = 10;
console.log(myNumber);


// opérateur d'incrémentation
myNumber++;
console.log(myNumber);

// opérateur d'incrémentation
myNumber--;
console.log(myNumber);

// opérateur d'addition/concaténation: +
var myAdd = 7 + 5;
console.log(myAdd);
var myConc = 'salut' + 'mon' + 'pote';
console.log(myConc);

// opérateur de soustraction: 6
var mySous = 7 - 5;
console.log(mySous);

// opérateur de multiplication
var myMult = 7 * 5;
console.log(myMult);
// opérateur de division
var myDiv = 14/3;
console.log(myDiv);

// opérateur modulo:%(le modulo est le reste d'une dévision)
var myModulo = 12 % 5;
console.log(myModulo);

// opérateur d'addition et d'affectation :+=
var first = 10;
var second = 5;

// first=first+second
first += second;
console.log(first);

// opérateur de soustraction et d'affectation
second -= first;
console.log(second);
