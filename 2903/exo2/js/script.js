var myNumber = 45;

// égalité simple:vérifier seulement la valeure de la variable

console.log( myNumber == 45 );
console.log( myNumber == "45" );

//  inégalité simple

console.log( myNumber != 45 );
console.log( myNumber != "45" );
console.log( myNumber != 12 );
console.log( myNumber != "12" );

// égalité stricte:vérifier la valeur et le type de la variable

console.log( myNumber === 45 );
console.log( myNumber === "45" );

// inégalité stricte
console.log( myNumber !== 45 );
console.log( myNumber !== "45" );
console.log( myNumber !== 12 );
console.log( myNumber !== "12" );

// supérieur/inférieur
console.log( myNumber > 46 );
console.log( myNumber < 46 );

// supérieur ou égale/inférieur ou égale
console.log( myNumber >= 12 );
console.log( myNumber <= 20 );

console.log( myNumber >= 45 );
console.log( myNumber <= 45 );
